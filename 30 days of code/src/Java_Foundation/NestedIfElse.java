package Java_Foundation;

public class NestedIfElse {

	public static void main(String[] args) {
		int x=10;
		int y=20;
		if(x==y)
		{
			System.out.println(x+" Is equal to"+y);
		}
		else
		{
			if(x>y)
			{
				System.out.println(x+" is Greater than"+y);
			}
			else
			{
			System.out.println(x +" is smaller than "+y);
			}
		}
		
	
	}
}
